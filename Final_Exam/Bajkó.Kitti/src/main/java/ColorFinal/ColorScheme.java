package ColorFinal;

import java.awt.*;

public class ColorScheme {
    Color lightBlue = new Color(153, 221, 255);
    Color lightGreen = new Color(153, 255, 153);
    Color lightRed = new Color(255, 77, 77);

    public Color getLightBlue() {
        return lightBlue;
    }

    public Color getLightGreen() {
        return lightGreen;
    }

    public Color getLightRed() {
        return lightRed;
    }
}
